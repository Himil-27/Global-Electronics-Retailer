	WITH CTE AS
	(SELECT ORDER_DATE, ORDER_NUMBER, LINE_ITEM, CURRENCY_CODE, PRODUCT_KEY, STORE_KEY, QUANTITY, EXCHANGE
	FROM SALES S
	JOIN EXCHANGE_RATES ER
	ON S.ORDER_DATE = ER.[DATE] AND S.CURRENCY_CODE = ER.CURRENCY
	WHERE STORE_KEY != 0),

	CTE2 AS
	(SELECT ORDER_NUMBER, LINE_ITEM, QUANTITY, EXCHANGE*UNIT_PRICE_USD AS 'UNIT_PRICE'
	FROM CTE 
	JOIN PRODUCTS P
	ON CTE.PRODUCT_KEY = P.PRODUCT_KEY
	GROUP BY ORDER_NUMBER, LINE_ITEM, QUANTITY, EXCHANGE, UNIT_PRICE_USD),

	CTE3 AS
	(SELECT ORDER_NUMBER, SUM(QUANTITY*UNIT_PRICE) AS 'ORDER_VALUE'
	FROM CTE2
	GROUP BY ORDER_NUMBER)

	SELECT ROUND(SUM(ORDER_VALUE)/COUNT(ORDER_NUMBER),3) AS 'AOV_IN_STORE'
	FROM CTE3;

	-- ONLINE

	WITH CTE AS
	(SELECT ORDER_DATE, ORDER_NUMBER, LINE_ITEM, CURRENCY_CODE, PRODUCT_KEY, QUANTITY, EXCHANGE
	FROM SALES S
	JOIN EXCHANGE_RATES ER
	ON S.ORDER_DATE = ER.[DATE] AND S.CURRENCY_CODE = ER.CURRENCY
	WHERE STORE_KEY = 0),

	CTE2 AS
